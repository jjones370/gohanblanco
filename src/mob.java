
public class mob {
	public int atk;
	public int hp;
	public int spd;
	public int def;
	public int spatk;
	public int spdef;
	
public int getHp()
	{
	return hp;
	}
public int getDefense()
	{
	return def;
	}
public int getSpeed()
	{
	return spd;
	}
public int getSpecialD()
	{
	return spdef;
	}
public int getAttack()
	{
	return atk;
	}	
public int getSpecialA()
	{
	return spatk;
	}	
}
